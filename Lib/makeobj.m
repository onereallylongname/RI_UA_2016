function [ P ,f, cf, p0] = makeobj( n, dim )
%=============================================================================%
%MAKEOBJ - makeobj( n , dim )
%   Obj 0 - Floor;
%   Obj 1 - Palete; Param: n = 1; dim - not needed
%   Obj 2 - Suporte da peça; Param: n = 2; dim = HS
%   Obj 3 - Peça a gravar; Param: n = 3; dim = [L H]
%   Output [P, f, cf], made for patch.
%=============================================================================%
%=============================================================================%
%> Objeto 0 - Chão
%=============================================================================%
    if n == 0
        % obj1  x   y   z
        P(1,:)=[0,      0,      0  ];
        P(2,:)=[dim(1), 0,      0  ];
        P(3,:)=[dim(1), dim(2), 0  ];
        P(4,:)=[0,      dim(2), 0  ];
        %faces
        f(1,:)=[1, 2, 3, 4];
        %cores
        cf = [0.5 0.5 0.5];
        %center position
        p0 = [dim(1)/2, dim(2)/2, 0];
    end
    
%=============================================================================%
%> Objeto 1 - Paletes
%=============================================================================%
    if n == 1
        % obj1  x   y   z
        P(1,:)=[0  ,0  ,0  ];
        P(2,:)=[96 ,0  ,0  ];
        P(3,:)=[96 ,93 ,0  ];
        P(4,:)=[0  ,93 ,0  ];
        P(5,:)=[0  ,0  ,10 ];
        P(6,:)=[96 ,0  ,10 ];
        P(7,:)=[96 ,93 ,10 ];
        P(8,:)=[0  ,93 ,10 ];
        %faces
        f(1,:)=[1, 2, 3, 4];
        f(2,:)=[1, 2, 6, 5];
        f(3,:)=[2, 3, 7, 6];
        f(4,:)=[3, 4, 8, 7];
        f(5,:)=[4, 1, 5, 8];
        f(6,:)=[5, 6, 7, 8];
        %cores
        cf = 0.35*ones(6,3);
        cf(:,1) = cf(:,1)+0.2;
        cf(:,2) = cf(:,2)+0.1;
        %center position - top face
        p0 = [96/2, 93/2, 10];
    end
    
%=============================================================================%
%> Objeto 2 - Sporte
%=============================================================================%
    if n == 2
         HS = dim;
        % obj1  x   y   z
        P(1,:)=[0  ,0  ,0  ];
        P(2,:)=[45 ,0  ,0  ];
        P(3,:)=[45 ,30 ,0  ];
        P(4,:)=[0  ,30 ,0  ];
        P(5,:)=[0  ,0  ,HS ];
        P(6,:)=[45 ,0  ,HS ];
        P(7,:)=[45 ,30 ,HS ];
        P(8,:)=[0  ,30 ,HS ];
        %faces
        f(1,:)=[1, 2, 3, 4];
        f(2,:)=[1, 2, 6, 5];
        f(3,:)=[2, 3, 7, 6];
        f(4,:)=[3, 4, 8, 7];
        f(5,:)=[4, 1, 5, 8];
        f(6,:)=[5, 6, 7, 8];
        %cores
        cf = 0.2*ones(6,3);
        %center position - top face
        p0 = [45/2, 5, HS];
    end 
    
%=============================================================================%
%> Objeto 3 - Peça
%=============================================================================%
    if n == 3
        L = dim(1);
        H = dim(2);
        % obj1  x   y   z
        P(1,:)=[0  ,0  ,-H ];
        P(2,:)=[30 ,0  ,-H ];
        P(3,:)=[30 ,45 ,-H];
        P(4,:)=[0  ,45 ,-H ];
        P(5,:)=[0  ,0  ,0 ];
        P(6,:)=[L  ,0  ,0 ];
        P(7,:)=[L  ,45 ,0 ];
        P(8,:)=[0  ,45 ,0 ];
        P(9,:)=[30 ,0  ,5-H];
        P(10,:)=[30  ,45 ,5-H];
        P = transformobj(P, -5, -45/2, 0, 0);
        %faces
        f(1,:)=[1 ,2 ,3 ,4 ,4];
        f(2,:)=[5 ,6 ,7 ,8 ,4];
        f(3,:)=[6 ,9 ,10,7 ,6];
        f(4,:)=[2 ,3 ,10,9 ,2];
        f(5,:)=[1 ,4 ,8 ,5 ,5];
        f(6,:)=[1 ,2 ,9 ,6 ,5];
        f(7,:)=[4 ,3 ,10,7 ,8];
        %cores
        cf = zeros(7,3);
        cf(:,1) = 0.8*ones(7,1);
        %center position - top face
        p0 = [0, 0, 0];
    end 
end

